#include <iostream>

using namespace std;

void printTitleSep(const std::string& title) {
    cout << "\n=====================================\n";
    cout << "     === " << title << " ===\n";
    cout << "=====================================\n";
}

void printSmallSep(const std::string& title) {
    cout << "--- " << title << " ---\n";
}
